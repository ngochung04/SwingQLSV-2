    private void JtbThongTinSVMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtbThongTinSVMouseClicked

        int row =JtbThongTinSV.getSelectedRow();

        txtMssv.setText(JtbThongTinSV.getModel().getValueAt(row, 0).toString());
        txtHovaten.setText(JtbThongTinSV.getModel().getValueAt(row, 1).toString());
        if(JtbThongTinSV.getModel().getValueAt(row, 2).toString().equals("nam")){
            radNam.setSelected(true);
            radNu.setSelected(false);
        }else{
            radNu.setSelected(true);
            radNam.setSelected(false);
        }
        txtNamSinh.setText(String.valueOf(svbll.Laynamsinh(Integer.parseInt(JtbThongTinSV.getModel().getValueAt(row, 3).toString()))));
        txtDiemToan.setText(JtbThongTinSV.getModel().getValueAt(row, 4).toString());
        txtDiemLy.setText(JtbThongTinSV.getModel().getValueAt(row, 5).toString());
        txtDiemHoa.setText(JtbThongTinSV.getModel().getValueAt(row, 6).toString());
        txtDTB.setText(JtbThongTinSV.getModel().getValueAt(row, 7).toString());
    }//GEN-LAST:event_JtbThongTinSVMouseClicked

    private void btnThemMoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemMoiActionPerformed
        SinhvienDTO sv = new SinhvienDTO();
        svdto = new SinhvienDTO();
        sv.setId(Integer.parseInt(String.valueOf(svbll.getmaxid())));
        sv.setTen(txtHovaten.getText());
        sv.setGioitinh(gender());
        sv.setNamsinh(Integer.parseInt(txtNamSinh.getText()));
        sv.setDiemtoan(Float.parseFloat(txtDiemToan.getText()));
        sv.setDiemly(Float.parseFloat(txtDiemLy.getText()));
        sv.setDiemhoa(Float.parseFloat(txtDiemHoa.getText()));
        sv.setDtb(svdto.tinhDTB());
        sv.setHocluc(svdto.xethocluc());
        svbll.themsinhvien(sv);
        svbll.HienthiDSSV(JtbThongTinSV);
    }//GEN-LAST:event_btnThemMoiActionPerformed

