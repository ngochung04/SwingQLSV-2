    private void btnLuuFileActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuFileActionPerformed
        // TODO add your handling code here:
        JFileChooser j = new JFileChooser("D:");

        // invoke the showsSaveDialog function to show the save dialog
        int r = j.showSaveDialog(null);

        // if the user selects a file
        if (r == JFileChooser.APPROVE_OPTION) {
            // set the label to the path of the selected file
            fileSavename = j.getSelectedFile().getAbsolutePath();
            try {
                svbll.ghifile(fileSavename, JtbThongTinSV);
            } catch (IOException ex) {
                java.util.logging.Logger.getLogger(SinhVienGUI.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            }
        } // if the user cancelled the operation
        else {
            System.out.println("the user cancelled the operation");
        }
    }//GEN-LAST:event_btnLuuFileActionPerformed

